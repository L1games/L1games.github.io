document.getElementById('petbtn').addEventListener('click', function () {
    const elementos0 = document.querySelectorAll('#vic20game');

    const elementos1 = document.querySelectorAll('#c64game');

    const elementos2 = document.querySelectorAll('#plus4game');

    const elementos3 = document.querySelectorAll('#amigagame');

    const elementos4 = document.querySelectorAll('#c128game');

    const elementos5 = document.querySelectorAll('#msdosgame');

    elementos0.forEach(function (elemento) {
        elemento.style.display = 'none';
    });

    elementos1.forEach(function (elemento) {
        elemento.style.display = 'none';
    });

    elementos2.forEach(function (elemento) {
        elemento.style.display = 'none';
    });

     elementos3.forEach(function (elemento) {
        elemento.style.display = 'none';
    });

     elementos4.forEach(function (elemento) {
        elemento.style.display = 'none';
    });

    elementos5.forEach(function (elemento) {
        elemento.style.display = 'none';
    });
});

